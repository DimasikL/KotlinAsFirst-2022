fun isPalindrome(n: Int): Boolean {
    if (n < 10) return true
    var num = n
    var sec = 0
    var fir: Int
    while (num > 0) {
        fir = num % 10
        num /= 10
        sec *= 10
        sec += fir
    }
    return n == sec
}


