 * Найти число Фибоначчи из ряда 1, 1, 2, 3, 5, 8, 13, 21, ... с номером n.
 * Ряд Фибоначчи определён следующим образом: fib(1) = 1, fib(2) = 1, fib(n+2) = fib(n) + fib(n+1)
 */
fun fib(n: Int): Int {
    var count = n
    var fNumber = 1
    var sNumber = 1
    var sum = 0
    if (count <= 2) return 1
    while (count > 2) {
        sum = fNumber + sNumber
        fNumber = sNumber
        sNumber = sum
        count -= 1


    }
    return sum
}

